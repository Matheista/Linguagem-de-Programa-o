package com.excript.aula025;

public class aula0025 {
	public static void main(String[] args){
		//Operadores de atribuição
		
		int x = 12;
		int y = 4;
		
		
		/*
		 * x+=y;
		 * x-=y;
		 * 
		 * x*=y;
		 * x/=y;
		 * 
		 * x%=y;
		 */
		
		x = 12;
		System.out.println(x+=y);
		System.out.println("----------");
		
		x = 12;
		System.out.println(x-=y);
		System.out.println("----------");

		
		x = 12;
		System.out.println(x*=y);
		System.out.println("----------");

		
		x = 12;
		System.out.println(x/=y);
		System.out.println("----------");

		
		x = 12;
		System.out.println(x%=y);
		System.out.println("----------");

	}

}
