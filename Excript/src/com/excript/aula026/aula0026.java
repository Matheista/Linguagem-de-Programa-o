package com.excript.aula026;

public class aula0026 {
	public static void main(String[] args){
		//Operadores de incremento I
		int i = 0;
		int y =0;
		
		
		i+=1;//++i PRE-incremento
		
		System.out.println(i);
		
		
		System.out.println(y++);
		System.out.println(y);
		i+=1;//i++ POS incremento

//		Pré--o valor vai ser incrementado antes da instrução aonde a variavel
//		estiver contida 
//		Pos--o valor vai ser incrementado apos a execução  aonde a nossa expressão
//		estiver continda
		
		int a = 0;
		int b = 0;
		
		System.out.println("--------------");
		System.out.println(--a);//PRE-Decremento
		
		System.out.println(b--);//Pos-decremento
		
		int u =0;
		
		u= u + 1;//Forma tracional
		u += 1;//Operador de atribuição
		++u;//Operadore de incremento
		
	
		
		
		
	}

}
